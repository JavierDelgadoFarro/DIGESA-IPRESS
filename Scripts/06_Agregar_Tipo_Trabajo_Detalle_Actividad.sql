-- 5. Agregar campos a tabla UTD_ATENCIONES
IF NOT EXISTS (SELECT * FROM sys.columns WHERE object_id = OBJECT_ID('UTD_ATENCIONES') AND name = 'ID_TIPO_TRABAJO')
BEGIN
    ALTER TABLE UTD_ATENCIONES
    ADD ID_TIPO_TRABAJO INT NULL;
    
    PRINT 'Columna ID_TIPO_TRABAJO agregada a UTD_ATENCIONES.';
END
ELSE
BEGIN
    PRINT 'La columna ID_TIPO_TRABAJO ya existe en UTD_ATENCIONES.';
END
GO

IF NOT EXISTS (SELECT * FROM sys.columns WHERE object_id = OBJECT_ID('UTD_ATENCIONES') AND name = 'ID_DETALLE_ACTIVIDAD')
BEGIN
    ALTER TABLE UTD_ATENCIONES
    ADD ID_DETALLE_ACTIVIDAD INT NULL;
    
    PRINT 'Columna ID_DETALLE_ACTIVIDAD agregada a UTD_ATENCIONES.';
END
ELSE
BEGIN
    PRINT 'La columna ID_DETALLE_ACTIVIDAD ya existe en UTD_ATENCIONES.';
END
GO

IF NOT EXISTS (SELECT * FROM sys.columns WHERE object_id = OBJECT_ID('UTD_ATENCIONES') AND name = 'NUMERO_EXPEDIENTE')
BEGIN
    ALTER TABLE UTD_ATENCIONES
    ADD NUMERO_EXPEDIENTE VARCHAR(50) NULL;
    
    PRINT 'Columna NUMERO_EXPEDIENTE agregada a UTD_ATENCIONES.';
END
ELSE
BEGIN
    PRINT 'La columna NUMERO_EXPEDIENTE ya existe en UTD_ATENCIONES.';
END
GO

-- 6. Agregar Foreign Keys
IF NOT EXISTS (SELECT * FROM sys.foreign_keys WHERE name = 'FK_ATENCIONES_TIPO_TRABAJO')
BEGIN
    ALTER TABLE UTD_ATENCIONES
    ADD CONSTRAINT FK_ATENCIONES_TIPO_TRABAJO
    FOREIGN KEY (ID_TIPO_TRABAJO) REFERENCES UTD_TIPO_TRABAJO(ID_TIPO_TRABAJO);
    
    PRINT 'Foreign Key FK_ATENCIONES_TIPO_TRABAJO creada correctamente.';
END
ELSE
BEGIN
    PRINT 'La Foreign Key FK_ATENCIONES_TIPO_TRABAJO ya existe.';
END
GO

IF NOT EXISTS (SELECT * FROM sys.foreign_keys WHERE name = 'FK_ATENCIONES_DETALLE_ACTIVIDAD')
BEGIN
    ALTER TABLE UTD_ATENCIONES
    ADD CONSTRAINT FK_ATENCIONES_DETALLE_ACTIVIDAD
    FOREIGN KEY (ID_DETALLE_ACTIVIDAD) REFERENCES UTD_DETALLE_ACTIVIDAD(ID_DETALLE_ACTIVIDAD);
    
    PRINT 'Foreign Key FK_ATENCIONES_DETALLE_ACTIVIDAD creada correctamente.';
END
ELSE
BEGIN
    PRINT 'La Foreign Key FK_ATENCIONES_DETALLE_ACTIVIDAD ya existe.';
END
GO

-- 7. Establecer valor por defecto PRESENCIAL para registros existentes
UPDATE UTD_ATENCIONES
SET ID_TIPO_TRABAJO = (SELECT ID_TIPO_TRABAJO FROM UTD_TIPO_TRABAJO WHERE NOMBRE_TIPO_TRABAJO = 'PRESENCIAL')
WHERE ID_TIPO_TRABAJO IS NULL;

PRINT 'Actualización completada: registros existentes establecidos con TIPO_TRABAJO = PRESENCIAL.';
GO

