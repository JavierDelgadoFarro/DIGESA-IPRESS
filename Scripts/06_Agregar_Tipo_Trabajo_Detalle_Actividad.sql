    PRINT 'Datos insertados en UTD_TIPO_TRABAJO.';
END
ELSE
BEGIN
    PRINT 'Los datos de UTD_TIPO_TRABAJO ya existen.';
END
GO

-- 4. Insertar datos en UTD_DETALLE_ACTIVIDAD
