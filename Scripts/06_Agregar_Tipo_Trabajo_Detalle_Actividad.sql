-- 1. Tabla de Tipo de Trabajo
IF NOT EXISTS (SELECT * FROM sys.tables WHERE name = 'UTD_TIPO_TRABAJO')
BEGIN
    CREATE TABLE UTD_TIPO_TRABAJO (
        ID_TIPO_TRABAJO INT PRIMARY KEY IDENTITY(1,1),
        NOMBRE_TIPO_TRABAJO VARCHAR(100) NOT NULL,
        DESCRIPCION VARCHAR(500),
        ESTADO BIT NOT NULL DEFAULT 1,
        FECHA_CREACION DATETIME NOT NULL DEFAULT GETDATE()
    );
    
    PRINT 'Tabla UTD_TIPO_TRABAJO creada correctamente.';
END
ELSE
BEGIN
    PRINT 'La tabla UTD_TIPO_TRABAJO ya existe.';
END
GO

-- 2. Tabla de Detalle de Actividad
IF NOT EXISTS (SELECT * FROM sys.tables WHERE name = 'UTD_DETALLE_ACTIVIDAD')
BEGIN
    CREATE TABLE UTD_DETALLE_ACTIVIDAD (
        ID_DETALLE_ACTIVIDAD INT PRIMARY KEY IDENTITY(1,1),
        NOMBRE_ACTIVIDAD VARCHAR(200) NOT NULL,
        DESCRIPCION VARCHAR(500),
        ESTADO BIT NOT NULL DEFAULT 1,
        FECHA_CREACION DATETIME NOT NULL DEFAULT GETDATE()
    );
    
    PRINT 'Tabla UTD_DETALLE_ACTIVIDAD creada correctamente.';
END
ELSE
BEGIN
    PRINT 'La tabla UTD_DETALLE_ACTIVIDAD ya existe.';
END
GO

